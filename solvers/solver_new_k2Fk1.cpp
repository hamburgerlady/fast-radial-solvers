void mexFunction(int nlhs, mxArray *plhs[], int nrhs, const mxArray *prhs[])
{
	if (nrhs != 1) {
		mexErrMsgIdAndTxt("automatic_generator_cvpr:new_k1Fk2_test_52:nrhs", "One input required.");
	}
	if (nlhs != 1) {
		mexErrMsgIdAndTxt("automatic_generator_cvpr:new_k1Fk2_test_52:nlhs", "One output required.");
	}    
	if (!mxIsDouble(prhs[0]) || mxIsComplex(prhs[0])) {
		mexErrMsgIdAndTxt("automatic_generator_cvpr:new_k1Fk2_test_52:notDouble", "Input data must be type double.");
	}
	if(mxGetNumberOfElements(prhs[0]) % 97 != 0) {
		mexErrMsgIdAndTxt("automatic_generator_cvpr:new_k1Fk2_test_52:incorrectSize", "Input size must be multiple of 97.");
	}
	int n_instances = mxGetNumberOfElements(prhs[0]) / 97;
	double *input = mxGetPr(prhs[0]);
	plhs[0] = mxCreateDoubleMatrix(2,48*n_instances,mxCOMPLEX);
	double* zr = mxGetPr(plhs[0]);
	double* zi = mxGetPi(plhs[0]);
	for(int k = 0; k < n_instances; k++) {
		const VectorXd data = Map<const VectorXd>(input + k*97, 97);
		MatrixXcd sols = solver_new_k1Fk2_test_52(data);
		Index offset = k*sols.size();
		for (Index i = 0; i < sols.size(); i++) {
			zr[i+offset] = sols(i).real();
			zi[i+offset] = sols(i).imag();
		}
	}
}


